def strToIntConvertor(strSet):
    intValue = int(strSet)
    print(f"Int value of the string passed is {intValue}")
    sum = intValue + intValue
    print(f"Sum of {intValue} passed is {sum}")
    sub = intValue - 25
    print(f"Subtraction of 25 from {intValue} passed is {sub}")

strToIntConvertor('392')
strToIntConvertor('124')
strToIntConvertor('567')
