a = int(input("Enter a number: "))
b = int(input("Enter another number: "))

sum = a + b
sub = a - b
mul = a * b
div = a / b
mod = a % b

print(f"Result of {a} + {b} is : {sum}")
print(f"Result of {a} - {b} is : {sub}")
print(f"Result of {a} * {b} is : {mul}")
print(f"Result of {a} / {b} is : {div}")
print(f"Result of {a} % {b} is : {mod}")