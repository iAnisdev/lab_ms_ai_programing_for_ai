user_input = float(input("Enter a floating number: "))

user_input_str  = str(user_input)

user_input_int = int(user_input)

print(f"Floating number: {user_input}, and type is {type(user_input)}")
print(f"String number: {user_input_str}, and type is {type(user_input_str)}")
print(f"Integer number: {user_input_int}, and type is {type(user_input_int)}")